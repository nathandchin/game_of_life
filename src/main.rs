fn print_map(xs: &Vec<Vec<i32>>, iter_num: u32, total_iters: u32) {
    for row in xs {
        for elt in row {
            if *elt == 0 {
                print!("░░");
            } else {
                print!("██");
            }
        }
        println!();
    }
    println!();
    println!("{}/{} iterations", iter_num, total_iters);
}
